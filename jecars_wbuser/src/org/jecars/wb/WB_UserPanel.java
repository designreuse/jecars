    public void refresh() {
      try {
        final JC_UserNode user = mClient.getUserAsUserNode();
        mUserName.setText(  user.getName() );
        mFirstName.setText( user.getFirstName() );
        mLastName.setText(  user.getLastName() );
        mEmail.setText(     user.getEmail() );
      } catch( Exception e ) {
        reportError( e );
      }
      return;
    }

    public void reportMessage( final String pMessage ) {
      JOptionPane.showMessageDialog( this, pMessage );
      return;
    }

    public void reportError( final Throwable pError ) {
      pError.printStackTrace();
      return;
    }

    /** setDefaultScript
     * 
     * @param pScript
     */
    public void setDefaultScript( final JCS_defaultScript pScript ) {
      mDefaultScript = pScript;
      try {
        mClient = pScript.getClient();
        refresh();
      } catch( Exception e ) {
        reportError( e );
      }
      return;
    }


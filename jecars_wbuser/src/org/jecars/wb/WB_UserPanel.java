    private void mChangePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mChangePasswordActionPerformed
      try {
        final JC_UserNode userNode = mClient.getUserAsUserNode();
        userNode.changePassword( mCurrentPassword.getPassword(), mNewPassword.getPassword() );
      } catch( JC_HttpException jhe ) {
        if (jhe.getHttpErrorCode().getErrorCode()==HttpURLConnection.HTTP_UNAUTHORIZED) {
          reportMessage( "Current password is not correct" );
        }
        reportError( jhe );
      } catch( JC_Exception je ) {
        reportError( je );
      }
      reportMessage( "New password is set" );
      return;
    }//GEN-LAST:event_mChangePasswordActionPerformed


    private void mOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mOkActionPerformed
      try {
        final JC_UserNode userNode = mClient.getUserAsUserNode();
        userNode.setFirstName( mFirstName.getText() );
        userNode.setLastName(  mLastName.getText() );
        userNode.setEmail( mEmail.getText() );
        userNode.save();
      } catch( JC_Exception je ) {
        reportError( je );
      }
      reportMessage( "New values is set" );
      mFrame.setVisible( false );
      mFrame.dispose();
      return;
    }//GEN-LAST:event_mOkActionPerformed

